// Practica con while en JS...
let cont = 1;

while (cont <= 100) {
    document.write(cont + " ");
    cont++;
}

// Practica con do while en JS...

let cont2 = 1;

do {
    document.write(' <h1>" ' + cont2 + ' "</h1> ');
    cont2++;
} while (cont2 <= 5);

// Practica con for en JS...

for (let cont = 1; cont <= 100; cont++) {
    document.write(cont + ' ');
}

// Practica con for...in en JS...

for (let cont = 1; cont <= 5; cont++) {
    document.write(' <h1>" ' + cont + ' "</h1> ');
}   